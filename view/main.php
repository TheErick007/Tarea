<p class="text-start">
  El juego consiste en que los niños se colocan dentro de sacos o costales y dan saltos para avanzar en una loca y divertida carrera hacia la meta.

  Es una actividad original en la que puede participar un gran número de niños, solo se debe cuidar que no estén muy cerca uno del otro al comenzar la carrera.
  
  Las instrucciones y reglas para jugar “carreras de sacos” o “acostalados” como también se le llama, son simples; los pequeños solo debe tratar de saltar sin salirse de los sacos.
  Los niños se colocan en la línea de salida y se determina el sitio hasta donde van a correr, usualmente deben llegar a la meta y regresar al punto de partida. El ganador es el niño que llegue primero a completar la carrera.          

</p>
