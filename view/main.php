<p class="text-start">
Para jugar debes preparar el círculo, dibújalo con tiza, de tamaño grande para que todos los participantes pongan su pie adentro.

A la figura se le dibuja otra de la misma forma en todo el medio, es allí donde se escribe la palabra “stop”. Luego se hacen divisiones, según el número de jugadores de cada partida.

En cada sección se escribe el nombre de una fruta, animal u objeto que ellos quieran. Uno de los participantes da inicio al juego, diciendo la frase “Declaro que mi peor enemigo es (dice el nombre de algún elemento que se haya escrito)".
Ahora, debe decir cuántos pasos dará para alcanzar al compañero que esté más cerca, si son suficientes para llegar, sumará un punto.
